  const LANGS = ["uk", "en", "cs", "de", "es", "zh"];
  window.LANGS = LANGS;

  function normalizeLang(code) {
    const c = (code || "uk").toLowerCase();
    return LANGS.includes(c) ? c : "uk";
  }

  function getStoredLang() {
    try {
      return normalizeLang(localStorage.getItem("lofi-lang"));
    } catch {
      return "uk";
    }
  }

  function setStoredLang(code) {
    try {
      localStorage.setItem("lofi-lang", normalizeLang(code));
    } catch {
      /**/
    }
  }

  window.currentLang = getStoredLang();

  window.setLang = function (code) {
    window.currentLang = normalizeLang(code);
    setStoredLang(window.currentLang);
    document.documentElement.lang =
      window.currentLang === "uk"
        ? "uk"
        : window.currentLang === "zh"
          ? "zh-Hans"
          : window.currentLang;
    window.dispatchEvent(new CustomEvent("lofi-lang"));
  };

  window.t = function (key) {
    const lang = normalizeLang(window.currentLang);
    return (STRINGS[lang] && STRINGS[lang][key]) || (STRINGS.en && STRINGS.en[key]) || key;
  };

  window.applyI18nDom = function () {
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      const key = el.getAttribute("data-i18n");
      const val = window.t(key);
      if (el.dataset.i18nAttr) {
        el.setAttribute(el.dataset.i18nAttr, val);
      } else {
        el.textContent = val;
      }
    });
    const pt = window.t("pageTitle");
    if (document.title !== pt) document.title = pt;
  };
